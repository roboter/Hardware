uint16_t pwmData[LED_MAX * 24 + 50];
uint8_t led_Data[LED_MAX][3];
uint8_t running = FALSE;
uint8_t brightness = 0;
uint8_t direction = TRUE;

void setLed(uint8_t LEDNum, uint8_t Red, uint8_t Green, uint8_t Blue) {
	led_Data[LEDNum][0] = Green;
	led_Data[LEDNum][1] = Red;
	led_Data[LEDNum][2] = Blue;
}

void send() {
	for (uint8_t led = 0; led != LED_MAX; led++) {
		uint32_t data = (led_Data[led][0] << 16) | (led_Data[led][1] << 8)
				| led_Data[led][2];
		for (int i = 23; i >= 0; i--) {
			pwmData[(24 - i + led * 24)] = (data & (1 << i)) ? 60 : 30;
		}
	}
	uint32_t start = LED_MAX * 24 - 1;
	for (uint32_t i = start; i < start + 50; i++) {
		pwmData[i] = 0;
	}

	HAL_TIM_PWM_Start_DMA(&htim2, TIM_CHANNEL_1, (uint32_t*) pwmData,
	LED_MAX * 24);
	running = TRUE;

}

void sendSingle(uint8_t Green, uint8_t Red, uint8_t Blue) {
	running = TRUE;
	uint32_t data = (Green << 16) | (Red << 8) | Blue;
	for (int i = 23; i >= 0; i--) {
		pwmData[i] = (data & (1 << i)) ? 66 : 33;
	}

	// HAL_TIM_PWM_Start_DMA(&htim1, TIM_CHANNEL_1, (uint32_t*) pwmData, 24);
	HAL_TIM_PWM_Start_DMA(&htim2, TIM_CHANNEL_1, (uint32_t*) pwmData, 24);
	running = TRUE;
}

void HAL_TIM_PWM_PulseFinishedCallback(TIM_HandleTypeDef *htim) {
	//HAL_TIM_PWM_Stop_DMA(&htim1, TIM_CHANNEL_1);
	HAL_TIM_PWM_Stop_DMA(&htim2, TIM_CHANNEL_1);
	running = FALSE;
}
