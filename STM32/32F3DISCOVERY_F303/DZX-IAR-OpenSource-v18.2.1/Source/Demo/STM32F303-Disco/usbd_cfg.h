#ifndef USBD_CFG_H__
#define USBD_CFG_H__


/*******************************************************************************************************************************************
    DEVICE STACK CONFIGURATION
*******************************************************************************************************************************************/
/*******************************************************************************************************************************************

    NUMBER OF DEVICE PORTS          The number of ports supported by the hardware to create USB devices.

*******************************************************************************************************************************************/
#define CFG_USBDEVICEPORTS          1

/*******************************************************************************************************************************************

    THREAD PRIORITY                 The priority for the thread that processes host requests within the USB device stack.

*******************************************************************************************************************************************/
#define CFG_USBDPRIORITY            PRIO_NORMAL

/*******************************************************************************************************************************************

    THREAD STACK SIZE               The size, in bytes, for the stack memory for the thread used to process requests for the USB device 
                                    stack. Must be large enough to accommodate one instance of the request and request response buffers.

*******************************************************************************************************************************************/
#define CFG_USBDSTACKSIZE           1024

/*******************************************************************************************************************************************

    REQUEST BUFFER SIZE             The size, in bytes, for the buffer used to receive requests from a host.

*******************************************************************************************************************************************/
#define CFG_USBDREQBUFSIZE          64

/*******************************************************************************************************************************************

    REQUEST RESPONSE BUFFER SIZE    The size, in bytes, for the buffer used to create responses to requests received from a host.

*******************************************************************************************************************************************/
#define CFG_USBDREQRESBUFSIZE       256

/*******************************************************************************************************************************************

    WRITE TIMEOUT                   The maximum amount of time, in kernel ticks, to wait for a write transfer to complete when sending 
                                    responses to requests from a host.

*******************************************************************************************************************************************/
#define CFG_USBDWRITETIMEOUT        500

/*******************************************************************************************************************************************

    READ TIMEOUT                    The maximum amount of time, in kernel ticks, to wait for a read transfer to complete when receiving
                                    data from a host.

*******************************************************************************************************************************************/
#define CFG_USBDREADTIMEOUT         500





/*******************************************************************************************************************************************
    DEVICE DEFAULT CONFIGURATION
*******************************************************************************************************************************************/
/*******************************************************************************************************************************************

    SELF POWERED                    The default indication of whether the device is self-powered (TRUE or FALSE).

*******************************************************************************************************************************************/
#define CFG_USBDCFGSELFPOWERED      FALSE

/*******************************************************************************************************************************************

    REMOTE WAKEUP                   The default indication of whether the device supports remote wakeup (TRUE or FALSE).

*******************************************************************************************************************************************/
#define CFG_USBDCFGREMOTEWAKEUP     FALSE

/*******************************************************************************************************************************************

    MAX POWER                       The default power consumption, in milliamps, of the device (0 - 500mA).

*******************************************************************************************************************************************/
#define CFG_USBDCFGMAXPOWER         500






/*******************************************************************************************************************************************
    MICROSOFT CONFIGURATION
*******************************************************************************************************************************************/
/*******************************************************************************************************************************************

    MICROSOFT DESCRIPTORS           Determines whether to include code for sending Microsoft OS descriptors.

*******************************************************************************************************************************************/
#define CFG_USBDMSFT                TRUE





/*******************************************************************************************************************************************
    DEVICE FIRMWARE UPDATE CONFIGURATION
*******************************************************************************************************************************************/
/*******************************************************************************************************************************************

    DFU ATTRIBUTES                  The default attributes that are reported from within the functional descriptor of a DFU interface.

*******************************************************************************************************************************************/
#define CFG_DFUDATTRIBUTES          0xF

/*******************************************************************************************************************************************

    DFU DETACH TIMEOUT              The default time, in milliseconds, a DFU interface will wait before performing a DETACH event.

*******************************************************************************************************************************************/
#define CFG_DFUDDETACHTIMEOUT       1000

/*******************************************************************************************************************************************

    DFU VERSION                     The default supported version of the DFU interface.

*******************************************************************************************************************************************/
#define CFG_DFUDBCDDFU              0x0101

/*******************************************************************************************************************************************

    DFU TRANSFER SIZE               The default size, in bytes, of the transfers performed by the DFU interface.

*******************************************************************************************************************************************/
#define CFG_DFUDTRANSFERSIZE        4096






/*******************************************************************************************************************************************
    CLOCK CONFIGURATION
*******************************************************************************************************************************************/
/*******************************************************************************************************************************************

    PLL 1 MULTIPLIER                The multiplier value assigned to the configuration register for PLL 1 that is used to generate the 
                                    clock source for the USB peripheral.

*******************************************************************************************************************************************/
#define CFG_CLKPLL1MSEL		        0x3

/*******************************************************************************************************************************************

    PLL 1 DIVIDER                   The divider value assigned to the configuration register for PLL 1 that is used to generate the 
                                    clock source for the USB peripheral.

*******************************************************************************************************************************************/
#define CFG_CLKPLL1PSEL		        0x0



#endif /* USBD_CFG_H__ */
